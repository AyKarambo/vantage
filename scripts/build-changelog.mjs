// Compile CHANGELOG.md into a TypeScript module the renderer bundle can import.
//
// WHY GENERATE INSTEAD OF FETCH: the renderer's CSP is `default-src 'none'` with no
// `connect-src` (renderer/index.html), so it cannot fetch *anything* at runtime — not a
// remote URL, not even a local file next to the bundle. Compiling the notes into the
// module graph is the only CSP-compatible way to show them, and it means "What's new"
// works offline by construction.
//
// WHY .ts AND NOT .json: renderer/tsconfig.json doesn't set `resolveJsonModule`, so
// importing a .json would break `npm run typecheck` even though esbuild would bundle it
// happily. A generated .ts module sidesteps that entirely.
//
// The output is git-ignored and regenerated by `npm run typecheck` and every build, so it
// can never drift from CHANGELOG.md.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const SOURCE = join(root, 'CHANGELOG.md');
const OUT = join(root, 'renderer', 'src', 'generated', 'changelog.ts');

/** `## 0.32.0 — 15 July 2026` / `## Unreleased` — an em dash or a hyphen both work. */
const HEADING = /^##\s+(?<version>\S+)\s*(?:[—-]\s*(?<date>.+))?\s*$/;
/** `### Added` / `### Fixed` / `### Changed` */
const SECTION = /^###\s+(?<section>.+?)\s*$/;
/** `- text` (top-level bullets only; nested/indented lines continue the previous one). */
const BULLET = /^-\s+(?<text>.+)$/;

/** Strip the markdown the renderer can't render: bold/italic/code markers and links. */
function plain(md) {
  return md
    .replace(/\[([^\]]+)\]\([^)]*\)/g, '$1') // [text](url) -> text
    .replace(/\*\*([^*]+)\*\*/g, '$1')
    .replace(/(^|\W)\*([^*]+)\*(?=\W|$)/g, '$1$2')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Parse CHANGELOG.md into `ChangelogEntry`s (see src/core/whatsNew.ts).
 *
 * Only `##` version headings start an entry, so the file's preamble is ignored. A
 * `### Section` label is folded into each of its bullets ("Fixed: ..."), because
 * ChangelogEntry carries flat notes and the Added/Changed/Fixed distinction is worth
 * keeping for the reader.
 * @param {string} md raw CHANGELOG.md
 * @returns {{version: string, date?: string, notes: string[]}[]}
 */
export function parseChangelog(md) {
  const entries = [];
  let current = null;
  let section = '';
  let pending = null;

  const flush = () => {
    if (pending && current) current.notes.push(plain(section ? `${section}: ${pending}` : pending));
    pending = null;
  };

  for (const raw of String(md ?? '').split(/\r?\n/)) {
    const line = raw.trimEnd();

    const heading = HEADING.exec(line);
    if (heading) {
      flush();
      current = { version: heading.groups.version.trim().toLowerCase(), notes: [] };
      const date = heading.groups.date?.trim();
      if (date) current.date = date;
      entries.push(current);
      section = '';
      continue;
    }

    if (!current) continue; // still in the preamble

    const sec = SECTION.exec(line);
    if (sec) {
      flush();
      section = sec.groups.section.trim();
      continue;
    }

    const bullet = BULLET.exec(line);
    if (bullet) {
      flush();
      pending = bullet.groups.text;
      continue;
    }

    // A wrapped continuation line of the current bullet.
    if (pending && line.trim() && /^\s/.test(raw)) {
      pending += ` ${line.trim()}`;
      continue;
    }

    flush();
  }
  flush();

  return entries.filter((e) => e.notes.length > 0);
}

/** Render the parsed entries as a typed, generated TS module. */
export function renderModule(entries) {
  const body = JSON.stringify(entries, null, 2).replace(/^/gm, '  ').trim();
  return `// GENERATED FILE — do not edit, and don't commit it (it is git-ignored).
// Source: CHANGELOG.md · Generator: scripts/build-changelog.mjs
// Regenerated by \`npm run typecheck\` and every renderer build, so it cannot drift.
import type { ChangelogEntry } from '../../../src/core/whatsNew';

/** Every released version's user-facing notes, newest first as written in CHANGELOG.md. */
export const CHANGELOG: readonly ChangelogEntry[] = ${body};
`;
}

/**
 * Read CHANGELOG.md and (re)write the generated renderer module. Exported so the
 * renderer/preview builds can call it in-process rather than shelling out.
 * @returns {{version: string, date?: string, notes: string[]}[]} the parsed entries
 */
export function generateChangelogModule() {
  const entries = parseChangelog(readFileSync(SOURCE, 'utf8'));
  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, renderModule(entries), 'utf8');
  return entries;
}

function main() {
  const entries = generateChangelogModule();
  const stamped = entries.filter((e) => /^\d+\.\d+\.\d+$/.test(e.version)).length;
  console.log(`[build-changelog] ${entries.length} entries (${stamped} released) → renderer/src/generated/changelog.ts`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main();
}
